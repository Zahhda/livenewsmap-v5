// Helper function to render icons
export const renderIcon = (iconName, size = 20, className = '') => {
  const icon = Icons[iconName]
  if (!icon) {
    console.warn(`Icon "${iconName}" not found`)
    return ''
  }
  return icon(size, className)
}

// Icon components for easy use
export const Icon = ({ name, size = 20, className = '', ...props }) => {
  return {
    innerHTML: renderIcon(name, size, className),
    ...props
  }
}
